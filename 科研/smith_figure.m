freq=(1:100)*1e9;
%计算Π型网络的Y参数
C1=1e-14;
C2=5e-15;
C3=2e-14;
y11=1j*2*pi*freq*(C1+C2);
y22=1j*2*pi*freq*(C3+C2);
y12=-1j*2*pi*freq*C2;
y21=y12;
ypar_1=zeros(2,2,100);
ypar_1(1,1,:)=y11;
ypar_1(1,2,:)=y12;
ypar_1(2,1,:)=y21;
ypar_1(2,2,:)=y22;
y_obj_1=sparameters(ypar_1,freq);

%计算T型网络的Z参数
L1=1e-11;
L2=2e-11;
L3=5e-12;
z11=1j*2*pi*freq*(L1+L3);
z22=1j*2*pi*freq*(L2+L3);
z12=1j*2*pi*freq*L3;
z21=z12;
zpar=zeros(2,2,100);
zpar(1,1,:)=z11;
zpar(1,2,:)=z12;
zpar(2,1,:)=z21;
zpar(2,2,:)=z22;
%将Z参数转换为Y参数
ypar_2=z2y(zpar);
y_obj_2=sparameters(ypar_2,freq);

%将并联电路两只路的Y参数相加
%将整体Y参数转换为S参数并绘制Smith圆图
ypar=ypar_1+ypar_2;
spar=y2s(ypar);
s_obj=sparameters(spar,freq);
smith(s_obj,1,1);
